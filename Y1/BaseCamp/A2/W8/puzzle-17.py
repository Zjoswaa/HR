total_value = 0


def sort_collection(collection: list) -> dict:
    new_collection = {}
    collection.sort()
    for item in collection:
        if item[0] not in new_collection:
            new_collection[item[0]] = [int(item[1:])]
        else:
            new_collection[item[0]].append(int(item[1:]))
    return new_collection


